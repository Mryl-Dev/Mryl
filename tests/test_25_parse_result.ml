// ============================================================
// Test 25: parse_int / parse_f64 の Result 返し (issue #42)
//   A. parse_int  Ok 経路   (C1: 有効な整数文字列)
//   B. parse_int  Err 経路  (C1: 無効な文字列)
//   C. parse_f64  Ok 経路   (C1: 有効な浮動小数点文字列)
//   D. parse_f64  Err 経路  (C1: 無効な文字列)
//   E. parse_int を match で分岐してデータを利用する複合ケース
//
// カバレッジ観点:
//   C0  : parse_int / parse_f64 を各 1 回以上実行
//   C1  :
//     A/B: parse_int の Ok ブランチ / Err ブランチ 両方を実行
//     C/D: parse_f64 の Ok ブランチ / Err ブランチ 両方を実行
//   MC/DC:
//     parse_int 内 (parse 失敗) は単一条件のため C1 = MC/DC
//       T25-01: "42"   → 失敗=F → Ok  (false ブランチ; 単独決定)
//       T25-02: "abc"  → 失敗=T → Err (true  ブランチ; 単独決定)
//     MC/DC achieved: {T25-01, T25-02}
//
// 既知バグ:
//   Bug#7: Err(string) の中身を println で表示すると C native でゴミ値
//          Err アームでは文字列を print せずセンチネル値(-1)を返す
// ============================================================

// ----------------------------------------------------------
// ヘルパー: parse_int の結果を i32 で返す（Ok → 値, Err → -1）
// ----------------------------------------------------------
fn parse_int_or_neg(s: string) -> i32 {
    let r = parse_int(s);
    let v: i32 = match r {
        Ok(v)  => v,
        Err(e) => -1,
    };
    return v;
}

// ----------------------------------------------------------
// ヘルパー: parse_f64 の結果を番兵 i32 で返す（Ok → 1, Err → -1）
// Note: as キャスト (v0.3.0 未実装) を避けるため Ok の値は使わず 1 を返す
// ----------------------------------------------------------
fn parse_f64_or_sentinel(s: string) -> i32 {
    let r = parse_f64(s);
    let v: i32 = match r {
        Ok(v)  => 1,
        Err(e) => -1,
    };
    return v;
}

fn main() -> i32 {
    println("=== 25: parse Result ===");

    // ----------------------------------------------------------
    // A. parse_int Ok 経路                              (T25-01)
    // ----------------------------------------------------------
    let v1 = parse_int_or_neg("42");
    println(v1);        // 42

    // ----------------------------------------------------------
    // B. parse_int Err 経路                             (T25-02)
    // ----------------------------------------------------------
    let v2 = parse_int_or_neg("abc");
    println(v2);        // -1  (Bug#7 回避: Err の文字列は表示しない)

    // ----------------------------------------------------------
    // C. parse_f64 Ok 経路                              (T25-03)
    // ----------------------------------------------------------
    let v3 = parse_f64_or_sentinel("3.5");
    println(v3);        // 1  (Ok 経路に入ったことを確認)

    // ----------------------------------------------------------
    // D. parse_f64 Err 経路                             (T25-04)
    // ----------------------------------------------------------
    let v4 = parse_f64_or_sentinel("xyz");
    println(v4);        // -1

    // ----------------------------------------------------------
    // E. parse_int を連鎖して利用する複合ケース         (T25-05〜06)
    //    "10" と "3" をパースして加算
    // ----------------------------------------------------------
    let a = parse_int_or_neg("10");
    let b = parse_int_or_neg("3");
    let sum: i32 = a + b;
    println(sum);       // 13

    // 片方が無効な場合は -1 伝播を確認
    let c = parse_int_or_neg("10");
    let d = parse_int_or_neg("bad");
    if (d < 0) {
        println(-1);    // -1
    }

    println("=== OK ===");
    return 0;
}
