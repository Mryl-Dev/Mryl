// ============================================================
// Test 30: string.split() メソッド (fix #55)
//   A. 通常の区切り文字で分割（CSV）
//   B. 区切りが見つからない → 元の文字列を 1 要素で返す
//   C. 空文字列区切り → 1 文字ずつ分割
//   D. 複数文字の区切り
//   E. 先頭・末尾が区切り文字（空要素が生まれる）
//   F. スペース区切り
//   G. 関数の戻り値として string[] を受け取る
//
// カバレッジ観点:
//   C0  : split() を少なくとも 1 回実行
//   C1  :
//     A  : 複数要素に分割されるパス
//     B  : 区切りが見つからず要素数 1 になるパス
//     C  : delim=="" の空文字列分岐パス
//     E  : 先頭・末尾が区切り文字で空要素が生まれるパス
//   MC/DC:
//     D  : 複数文字区切り（1文字区切りとは別コードパス）
//     G  : 関数戻り値経由で string[] を受け取るパス
// ============================================================

// ----------------------------------------------------------
// G. 関数戻り値として string[] を受け取る
// ----------------------------------------------------------
fn split_and_count(s: string, delim: string) -> i32 {
    let parts: string[] = s.split(delim);
    return parts.len();
}

fn main() -> i32 {
    // ----------------------------------------------------------
    // A. 通常の区切り文字（CSV）
    // ----------------------------------------------------------
    let csv: string = "apple,banana,cherry";
    let parts: string[] = csv.split(",");
    println("A1: {}", parts.len());   // A1: 3
    println("A2: {}", parts[0]);      // A2: apple
    println("A3: {}", parts[1]);      // A3: banana
    println("A4: {}", parts[2]);      // A4: cherry

    // ----------------------------------------------------------
    // B. 区切りが見つからない
    // ----------------------------------------------------------
    let s1: string[] = "abc".split("x");
    println("B1: {}", s1.len());      // B1: 1
    println("B2: {}", s1[0]);         // B2: abc

    // ----------------------------------------------------------
    // C. 空文字列区切り → 1 文字ずつ
    // ----------------------------------------------------------
    let chars: string[] = "hello".split("");
    println("C1: {}", chars.len());   // C1: 5
    println("C2: {}", chars[0]);      // C2: h
    println("C3: {}", chars[1]);      // C3: e

    // ----------------------------------------------------------
    // D. 複数文字の区切り
    // ----------------------------------------------------------
    let s2: string[] = "abcXYZdef".split("XYZ");
    println("D1: {}", s2.len());      // D1: 2
    println("D2: {}", s2[0]);         // D2: abc
    println("D3: {}", s2[1]);         // D3: def

    // ----------------------------------------------------------
    // E. 先頭・末尾が区切り文字（空要素が生まれる）
    // ----------------------------------------------------------
    let s3: string[] = ",a,".split(",");
    println("E1: {}", s3.len());      // E1: 3

    // ----------------------------------------------------------
    // F. スペース区切り
    // ----------------------------------------------------------
    let words: string[] = "a b c".split(" ");
    println("F1: {}", words.len());   // F1: 3
    println("F2: {}", words[0]);      // F2: a
    println("F3: {}", words[1]);      // F3: b
    println("F4: {}", words[2]);      // F4: c

    // ----------------------------------------------------------
    // G. 関数戻り値として string[]
    // ----------------------------------------------------------
    let cnt = split_and_count("w:x:y:z", ":");
    println("G1: {}", cnt);           // G1: 4

    println("=== OK ===");
    return 0;
}
