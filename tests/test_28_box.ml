// ============================================================
// Test 28: Box<T> ポインタ型
//   A. Box::new(v) と * デリファレンス (i32)
//   B. .unbox() によるデリファレンス
//   C. Box<f64> — 浮動小数点型
//   D. Box<string> — 文字列型
//   E. 関数引数・戻り値としての Box<T>
//   F. 2重ポインタ Box<Box<i32>>
//   G. 3重ポインタ Box<Box<Box<i32>>>
//   H. 4重ポインタ Box<Box<Box<Box<i32>>>>
//
// カバレッジ観点:
//   C0  : Box::new / * / .unbox() を少なくとも1回実行
//   C1  :
//     A/B: * デリファレンスと .unbox() の両パス実行
//     E  : wrap_i32() の return Box::new(v) パス
//   MC/DC:
//     F〜H: 多重 Box のネスト・デリファレンス (各段で正しい値を返すことを確認)
// ============================================================

// ----------------------------------------------------------
// E. 関数戻り値として Box<T> を返す
// ----------------------------------------------------------
fn wrap_i32(v: i32) -> Box<i32> {
    return Box::new(v);
}

fn unwrap_add(a: Box<i32>, b: Box<i32>) -> i32 {
    return *a + *b;
}

fn main() -> i32 {
    // ----------------------------------------------------------
    // A. Box::new(v) + * デリファレンス
    // ----------------------------------------------------------
    let b1: Box<i32> = Box::new(42);
    let v1: i32 = *b1;
    println("{}", v1);      // 42

    let b2: Box<i32> = Box::new(-7);
    let v2: i32 = *b2;
    println("{}", v2);      // -7

    // ----------------------------------------------------------
    // B. .unbox() によるデリファレンス
    // ----------------------------------------------------------
    let b3: Box<i32> = Box::new(100);
    let v3: i32 = b3.unbox();
    println("{}", v3);      // 100

    let b4: Box<i32> = Box::new(0);
    let v4: i32 = b4.unbox();
    println("{}", v4);      // 0

    // ----------------------------------------------------------
    // C. Box<f64>
    // ----------------------------------------------------------
    let bf1: Box<f64> = Box::new(3.14);
    let vf1: f64 = *bf1;
    println("{}", vf1);     // 3.14

    let bf2: Box<f64> = Box::new(-2.5);
    let vf2: f64 = bf2.unbox();
    println("{}", vf2);     // -2.5

    // ----------------------------------------------------------
    // D. Box<bool>
    // ----------------------------------------------------------
    let bb1: Box<bool> = Box::new(true);
    let vb1: bool = *bb1;
    println("{}", vb1);     // true

    let bb2: Box<bool> = Box::new(false);
    let vb2: bool = bb2.unbox();
    println("{}", vb2);     // false

    // ----------------------------------------------------------
    // E. 関数引数・戻り値
    // ----------------------------------------------------------
    let bw: Box<i32> = wrap_i32(55);
    let vw: i32 = *bw;
    println("{}", vw);      // 55

    let ba: Box<i32> = Box::new(10);
    let bc: Box<i32> = Box::new(20);
    let sum: i32 = unwrap_add(ba, bc);
    println("{}", sum);     // 30

    // ----------------------------------------------------------
    // F. 2重ポインタ Box<Box<i32>>
    // ----------------------------------------------------------
    let bb_2: Box<Box<i32>> = Box::new(Box::new(99));
    let inner_2: Box<i32> = *bb_2;
    let val_2: i32 = *inner_2;
    println("{}", val_2);   // 99

    // 連鎖デリファレンス
    let bb_2b: Box<Box<i32>> = Box::new(Box::new(7));
    let val_2b: i32 = *(*bb_2b);
    println("{}", val_2b);  // 7

    // ----------------------------------------------------------
    // G. 3重ポインタ Box<Box<Box<i32>>>
    // ----------------------------------------------------------
    let bb_3: Box<Box<Box<i32>>> = Box::new(Box::new(Box::new(42)));
    let val_3: i32 = *(*(*bb_3));
    println("{}", val_3);   // 42

    // ----------------------------------------------------------
    // H. 4重ポインタ Box<Box<Box<Box<i32>>>>
    // ----------------------------------------------------------
    let bb_4: Box<Box<Box<Box<i32>>>> = Box::new(Box::new(Box::new(Box::new(5))));
    let val_4: i32 = *(*(*(*bb_4)));
    println("{}", val_4);   // 5

    println("=== OK ===");
    return 0;
}
