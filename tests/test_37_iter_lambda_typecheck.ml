// ============================================================
// Test 37: Iter<T> メソッドへのラムダ引数型検査 (fix #63)
//
// TypeChecker がラムダのパラメータ型・戻り値型を
// レシーバ配列の要素型と正しく照合することを確認する。
//
// ※ 型エラー検出は TypeChecker レベルの検査であり、
//    本テストは「正しい型アノテーション付きコードが通る」
//    ことを C0/C1 カバレッジで確認する正常系テスト。
//
//   A. filter: 正しい型 (fn(T)->bool)
//   B. select: 正しい型 (fn(T)->U)
//   C. for_each: 正しい型 (fn(T)->void)
//   D. any: 正しい型 (fn(T)->bool)
//   E. all: 正しい型 (fn(T)->bool)
//   F. select_many: 正しい型 (fn(T)->U[])
//   G. aggregate 初期値なし: 正しい型 (fn(T,T)->T)
//   H. aggregate 初期値あり: 正しい型 (fn(U,T)->U)
//   I. select → filter チェーン: 中間の elem_type が変わっても検査が通る
//   J. アノテーションなしラムダ: any 扱いでスキップ（通過）
//   K. 名前付き関数参照 (VarRef) を iter メソッドに渡す
//
// カバレッジ観点:
//   C0  : 全 iter メソッドを最低1回検査
//   C1  : アノテーションあり / アノテーションなし の両分岐
//   MC/DC: VarRef vs Lambda の両経路
// ============================================================

fn is_even(x: i32) -> bool {
    return x % 2 == 0;
}

fn double(x: i32) -> i32 {
    return x * 2;
}

fn main() -> i32 {
    let nums: i32[] = [1, 2, 3, 4, 5, 6];

    // ----------------------------------------------------------
    // A. filter: fn(i32)->bool
    // ----------------------------------------------------------
    let fa: i32[] = nums.filter((x: i32) => x % 2 == 0).to_array();
    println("A1: {}", fa.len());   // A1: 3
    println("A2: {}", fa[0]);      // A2: 2

    // ----------------------------------------------------------
    // B. select: fn(i32)->i32
    // ----------------------------------------------------------
    let fb: i32[] = nums.select((x: i32) => x * 10).to_array();
    println("B1: {}", fb.len());   // B1: 6
    println("B2: {}", fb[0]);      // B2: 10

    // ----------------------------------------------------------
    // C. for_each: fn(i32)->void（println の戻り値は捨てる）
    // ----------------------------------------------------------
    let fc: i32[] = [10, 20];
    fc.for_each((x: i32) => println("C: {}", x));
    // C: 10
    // C: 20

    // ----------------------------------------------------------
    // D. any: fn(i32)->bool
    // ----------------------------------------------------------
    let fd: bool = nums.any((x: i32) => x > 5);
    println("D1: {}", fd);         // D1: true

    // ----------------------------------------------------------
    // E. all: fn(i32)->bool
    // ----------------------------------------------------------
    let fe: bool = nums.all((x: i32) => x > 0);
    println("E1: {}", fe);         // E1: true

    // ----------------------------------------------------------
    // F. select_many: fn(i32)->i32[]
    // ----------------------------------------------------------
    let small: i32[] = [1, 2, 3];
    let ff: i32[] = small.select_many((x: i32) => [x, x * 10]).to_array();
    println("F1: {}", ff.len());   // F1: 6
    println("F2: {}", ff[0]);      // F2: 1
    println("F3: {}", ff[1]);      // F3: 10

    // ----------------------------------------------------------
    // G. aggregate 初期値なし: fn(i32,i32)->i32
    // ----------------------------------------------------------
    let fg = small.aggregate((a: i32, b: i32) => a + b);
    match fg {
        Ok(v)  => println("G1: {}", v),   // G1: 6
        Err(e) => println("G1: err"),
    };

    // ----------------------------------------------------------
    // H. aggregate 初期値あり: fn(i32,i32)->i32
    // ----------------------------------------------------------
    let fh: i32 = small.aggregate(0, (acc: i32, x: i32) => acc + x);
    println("H1: {}", fh);         // H1: 6

    // ----------------------------------------------------------
    // I. select → filter チェーン: 中間 elem_type は i32 のまま
    // ----------------------------------------------------------
    let fi: i32[] = nums
        .select((x: i32) => x * 2)
        .filter((x: i32) => x > 6)
        .to_array();
    println("I1: {}", fi.len());   // I1: 3
    println("I2: {}", fi[0]);      // I2: 8

    // ----------------------------------------------------------
    // J. アノテーションなしラムダ: any 扱いでスキップ（正常通過）
    // ----------------------------------------------------------
    let fj: i32[] = nums.filter((x) => x > 3).to_array();
    println("J1: {}", fj.len());   // J1: 3

    // ----------------------------------------------------------
    // K. 名前付き関数参照 (VarRef) を iter メソッドに渡す
    // ----------------------------------------------------------
    let fk_filter: i32[] = nums.filter(is_even).to_array();
    println("K1: {}", fk_filter.len());  // K1: 3
    println("K2: {}", fk_filter[0]);     // K2: 2

    let fk_select: i32[] = nums.select(double).to_array();
    println("K3: {}", fk_select.len());  // K3: 6
    println("K4: {}", fk_select[0]);     // K4: 2

    println("=== OK ===");
    return 0;
}
