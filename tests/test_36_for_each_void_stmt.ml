// Test 36: for_each void statement expression 修正確認 (fix #64)
//
//   A. for_each 単体（ExprStmt として直接使用）
//   B. for_each でキャプチャあり lambda（環境付き fat pointer）
//   C. filter → for_each チェーン（src_is_temp=True パス）
//   D. select → for_each チェーン（中間 MrylVec 解放確認）
//   E. 空配列への for_each（0 回実行 = 境界値）
//   F. for_each を if ブランチ内で使用（ネスト確認）
//
// カバレッジ観点:
//   C0  : for_each の各コード経路を少なくとも 1 回実行
//   C1  : 空配列（0 件）/ 非空配列（n 件）の両ブランチ
//   MC/DC: src_is_temp=True（チェーン）/ False（直接）両分岐
// ============================================================

fn main() -> i32 {

    // ----------------------------------------------------------
    // A. for_each 単体（ExprStmt として直接使用）
    // ----------------------------------------------------------
    let nums: i32[] = [1, 2, 3];
    nums.for_each((x: i32) => println("A: {}", x));
    // A: 1
    // A: 2
    // A: 3

    // ----------------------------------------------------------
    // B. for_each でキャプチャあり lambda
    // ----------------------------------------------------------
    let prefix: i32 = 100;
    nums.for_each((x: i32) => println("B: {}", x + prefix));
    // B: 101
    // B: 102
    // B: 103

    // ----------------------------------------------------------
    // C. filter → for_each チェーン（src_is_temp=True）
    // ----------------------------------------------------------
    nums.filter((x: i32) => x > 1).for_each((x: i32) => println("C: {}", x));
    // C: 2
    // C: 3

    // ----------------------------------------------------------
    // D. select → for_each チェーン（中間 MrylVec 解放）
    // ----------------------------------------------------------
    nums.select((x: i32) => x * 10).for_each((x: i32) => println("D: {}", x));
    // D: 10
    // D: 20
    // D: 30

    // ----------------------------------------------------------
    // E. 空配列への for_each（境界値: 0 回実行）
    // ----------------------------------------------------------
    let empty: i32[] = [];
    empty.for_each((x: i32) => println("E: {}", x));
    println("E: done");
    // E: done

    // ----------------------------------------------------------
    // F. for_each を if ブランチ内で使用（ネスト確認）
    // ----------------------------------------------------------
    let flag: bool = true;
    if (flag) {
        nums.for_each((x: i32) => println("F: {}", x));
    }
    // F: 1
    // F: 2
    // F: 3

    println("=== OK ===");
    return 0;
}
