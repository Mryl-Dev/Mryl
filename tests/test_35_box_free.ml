// ============================================================
// Test 35: Box<T> メモリ解放
//   A. 基本 Box<T> のスコープ終了時 free
//   B. 関数パラメータ渡し（double free しないこと）
//   C. 関数戻り値 Box<T>（呼び元でスコープ終了時 free）
//   D. Box<Box<i32>> 多重ネスト（中間変数あり）
//   E. Box<Box<i32>> 多重ネスト（中間変数なし）
//   F. Box<Box<Box<i32>>> 3重ネスト（中間変数なし）
//   G. while ループ内 Box（各イテレーション末に free）
//   H. Vec<Box<i32>> 要素ごと free + .data free
//
// カバレッジ観点:
//   C0  : 各 Box パターンを少なくとも1回実行
//   C1  : inner_moved あり/なし の両パス
//   MC/DC: D（inner_moved パス）vs E（ネスト free パス）
// ============================================================

fn use_box(b: Box<i32>) -> i32 {
    return *b;
}

fn make_box(v: i32) -> Box<i32> {
    return Box::new(v);
}

fn main() -> i32 {
    // ----------------------------------------------------------
    // A. 基本 Box のスコープ終了時 free
    // ----------------------------------------------------------
    let a1: Box<i32> = Box::new(10);
    println("{}", *a1);         // 10

    let a2: Box<f64> = Box::new(3.14);
    println("{}", *a2);         // 3.14

    // ----------------------------------------------------------
    // B. 関数パラメータ渡し（double free しないこと）
    //    ba は main の local_box_vars で管理。use_box 内では free しない。
    // ----------------------------------------------------------
    let ba: Box<i32> = Box::new(42);
    let vb: i32 = use_box(ba);
    println("{}", vb);          // 42

    // ----------------------------------------------------------
    // C. 関数戻り値 Box（呼び元でスコープ終了時 free）
    // ----------------------------------------------------------
    let bc: Box<i32> = make_box(99);
    println("{}", *bc);         // 99

    // ----------------------------------------------------------
    // D. Box<Box<i32>> 多重ネスト（中間変数あり）
    //    inner_2 = *bb_2 → bb_2 を inner_moved マーク
    //    free(inner_2) + free(bb_2) のみ（二重解放なし）
    // ----------------------------------------------------------
    let bb_2: Box<Box<i32>> = Box::new(Box::new(77));
    let inner_2: Box<i32> = *bb_2;
    println("{}", *inner_2);    // 77

    // ----------------------------------------------------------
    // E. Box<Box<i32>> 多重ネスト（中間変数なし）
    //    bb_2b は inner_moved なし → free(*bb_2b); free(bb_2b)
    // ----------------------------------------------------------
    let bb_2b: Box<Box<i32>> = Box::new(Box::new(55));
    let val_e: i32 = *(*bb_2b);
    println("{}", val_e);       // 55

    // ----------------------------------------------------------
    // F. Box<Box<Box<i32>>> 3重ネスト（中間変数なし）
    //    free(**bb_3); free(*bb_3); free(bb_3)
    // ----------------------------------------------------------
    let bb_3: Box<Box<Box<i32>>> = Box::new(Box::new(Box::new(33)));
    let val_f: i32 = *(*(*bb_3));
    println("{}", val_f);       // 33

    // ----------------------------------------------------------
    // G. while ループ内 Box（各イテレーション末に free）
    // ----------------------------------------------------------
    let i: i32 = 0;
    while (i < 3) {
        let lb: Box<i32> = Box::new(i * 10);
        println("{}", *lb);     // 0, 10, 20
        i = i + 1;
    }

    // ----------------------------------------------------------
    // H. Vec<Box<i32>>: 各要素 free → .data free
    //    スコープ終了時に free(boxes.data[i]) × 3 + free(boxes.data) を生成する
    // ----------------------------------------------------------
    let boxes: Box<i32>[] = [Box::new(1), Box::new(2), Box::new(3)];
    println("boxes_len={}", boxes.len());   // 3

    println("=== OK ===");
    return 0;
}
