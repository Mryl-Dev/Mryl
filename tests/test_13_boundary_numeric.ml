// ============================================================
// Test 13: 数値型境界値テスト
//   A. i8 境界値 (min/min+1/0/max-1/max)
//   B. u8 境界値
//   C. i16 境界値
//   D. i32 境界値
//   E. 算術演算境界値 + C1 + MC/DC
//   F. i64 境界値 (i32 範囲外の値) / Bug#48 regression
//   G. u32 境界値 (i32 範囲外の値) / Bug#48 regression
//   H. u64 境界値 (i32 範囲外の値) / Bug#48 regression
//
// カバレッジ観点:
//   C0  : 全境界値変数を宣言使用して実行
//   C1  :
//     E: is_i32_max() の if(n==MAX)  true(n=MAX) / false(n=0) 両ブランチ
//   MC/DC:
//     E: in_byte_range(n) 内 (n >= -128 && n <= 127):
//       {n>=-128=F, *}           0 (n=-129: n>=-128=F 単独決定)
//       {n>=-128=T, n<=127=F}    0 (n=128:  n<=127=F 単独決定)
//       {n>=-128=T, n<=127=T}    1 (n=0:    両方 T)
//
// 境界外テスト: 実行時 UB のため省略
//   i8  min-1 (-129) / max+1 (128)  C UB (仕様 6.1)
//   i32 MAX+1                       signed overflow UB (6.4)
//   div 0                           SIGFPE (6.3)
// ============================================================

// C1 ヘルパー
fn is_i32_max(n: i32) -> i32 {
    if (n == 2147483647) {
        return 1;
    }
    return 0;
}

// MC/DC ヘルパー: (n >= -128 && n <= 127)
fn in_byte_range(n: i32) -> i32 {
    if (n >= -128 && n <= 127) {
        return 1;
    }
    return 0;
}

fn main() -> i32 {
    println("=== 13: Numeric Boundary ===");

    // ----------------------------------------------------------
    // A. i8 境界値 / C0
    // T13-01: min=-128  T13-02: min+1=-127  T13-03: zero=0
    // T13-04: max-1=126  T13-05: max=127
    // ----------------------------------------------------------
    println("--- A: i8 ---");
    let i8_min:  i8 = -128(i8);
    let i8_min1: i8 = -127(i8);
    let i8_zero: i8 =    0(i8);
    let i8_max1: i8 =  126(i8);
    let i8_max:  i8 =  127(i8);
    println("i8_min={}", i8_min);     // -128
    println("i8_min1={}", i8_min1);   // -127
    println("i8_zero={}", i8_zero);   // 0
    println("i8_max1={}", i8_max1);   // 126
    println("i8_max={}", i8_max);     // 127

    // ----------------------------------------------------------
    // B. u8 境界値 / C0
    // T13-06: min=0  T13-07: min+1=1  T13-08: mid=127
    // T13-09: max-1=254  T13-10: max=255
    // ----------------------------------------------------------
    println("--- B: u8 ---");
    let u8_min:  u8 =   0(u8);
    let u8_min1: u8 =   1(u8);
    let u8_mid:  u8 = 127(u8);
    let u8_max1: u8 = 254(u8);
    let u8_max:  u8 = 255(u8);
    println("u8_min={}", u8_min);     // 0
    println("u8_min1={}", u8_min1);   // 1
    println("u8_mid={}", u8_mid);     // 127
    println("u8_max1={}", u8_max1);   // 254
    println("u8_max={}", u8_max);     // 255

    // ----------------------------------------------------------
    // C. i16 境界値 / C0
    // T13-11: min=-32768  T13-12: min+1=-32767  T13-13: zero=0
    // T13-14: max-1=32766  T13-15: max=32767
    // ----------------------------------------------------------
    println("--- C: i16 ---");
    let i16_min:  i16 = -32768(i16);
    let i16_min1: i16 = -32767(i16);
    let i16_zero: i16 =      0(i16);
    let i16_max1: i16 =  32766(i16);
    let i16_max:  i16 =  32767(i16);
    println("i16_min={}", i16_min);    // -32768
    println("i16_min1={}", i16_min1);  // -32767
    println("i16_zero={}", i16_zero);  // 0
    println("i16_max1={}", i16_max1);  // 32766
    println("i16_max={}", i16_max);    // 32767

    // ----------------------------------------------------------
    // D. i32 境界値 / C0
    // T13-16: min=-2147483648  T13-17: min+1=-2147483647  T13-18: zero=0
    // T13-19: max-1=2147483646  T13-20: max=2147483647
    // ----------------------------------------------------------
    println("--- D: i32 ---");
    let i32_min:  i32 = -2147483648;
    let i32_min1: i32 = -2147483647;
    let i32_zero: i32 = 0;
    let i32_max1: i32 = 2147483646;
    let i32_max:  i32 = 2147483647;
    println("i32_min={}", i32_min);    // -2147483648
    println("i32_min1={}", i32_min1);  // -2147483647
    println("i32_zero={}", i32_zero);  // 0
    println("i32_max1={}", i32_max1);  // 2147483646
    println("i32_max={}", i32_max);    // 2147483647

    // ----------------------------------------------------------
    // E. 算術演算境界値 / C0
    // T13-21: 1+1=2  T13-22: MAX-1+1=MAX  T13-23: 0-1=-1
    // T13-24: MIN+1-1=MIN  T13-25: 100*100=10000
    // T13-26: 10/3=3  T13-27: 1/1=1  T13-28: 10%3=1  T13-29: 7%7=0
    // ----------------------------------------------------------
    println("--- E: Arithmetic ---");
    println("1+1={}", 1 + 1);                             // 2
    println("MAX-1+1={}", 2147483646 + 1);                // 2147483647
    println("0-1={}", 0 - 1);                             // -1
    println("MIN+1-1={}", -2147483647 - 1);               // -2147483648
    println("100*100={}", 100 * 100);                     // 10000
    println("10/3={}", 10 / 3);                           // 3
    println("1/1={}", 1 / 1);                             // 1
    println("10%3={}", 10 % 3);                           // 1
    println("7%7={}", 7 % 7);                             // 0

    // C1: is_i32_max
    println("--- E: C1 is_i32_max ---");
    // C1: n==MAX  true
    println("is_max(MAX)={}", is_i32_max(2147483647));    // 1
    // C1: n==MAX  false
    println("is_max(0)={}", is_i32_max(0));               // 0
    // C1: min に対しても false
    println("is_max(MIN)={}", is_i32_max(-2147483648));   // 0

    // MC/DC: in_byte_range (n >= -128 && n <= 127)
    println("--- E: MC/DC in_byte_range ---");
    // {n>=-128=F, *}  0 (n=-129)
    println("in_byte(-129)={}", in_byte_range(-129));     // 0
    // {n>=-128=T, n<=127=F}  0 (n=128)
    println("in_byte(128)={}", in_byte_range(128));       // 0
    // {n>=-128=T, n<=127=T}  1 (n=0)
    println("in_byte(0)={}", in_byte_range(0));           // 1
    // 境界値: n=-128  1, n=127  1
    println("in_byte(-128)={}", in_byte_range(-128));     // 1
    println("in_byte(127)={}", in_byte_range(127));       // 1

    // ----------------------------------------------------------
    // F. i64 境界値 (i32 範囲外) / C0  (Bug#48 regression)
    // T13-31: INT32_MAX+1=2147483648 → %d では -2147483648
    // ----------------------------------------------------------
    println("--- F: i64 ---");
    let i64_over: i64 = 2147483648(i64);
    println("i64_over={}", i64_over);  // 2147483648

    // ----------------------------------------------------------
    // G. u32 境界値 (i32 範囲外) / C0  (Bug#48 regression)
    // T13-32: INT32_MAX+1=2147483648 (u32) → %d では -2147483648
    // ----------------------------------------------------------
    println("--- G: u32 ---");
    let u32_over: u32 = 2147483648(u32);
    println("u32_over={}", u32_over);  // 2147483648

    // ----------------------------------------------------------
    // H. u64 境界値 (i32 範囲外) / C0  (Bug#48 regression)
    // T13-33: INT32_MAX+1=2147483648 (u64) → %d では -2147483648
    // ----------------------------------------------------------
    println("--- H: u64 ---");
    let u64_over: u64 = 2147483648(u64);
    println("u64_over={}", u64_over);  // 2147483648

    println("=== OK ===");
    return 0;
}
